// pass the input string by reference for backtracking
void permutations(vector<string> &ans, string &ip, int ind, int n)
{
    if (ind == n - 1)
    {
        ans.push_back(ip);
        return;
    }
    //unordered_set makes sure that inly unique permutations get prnted
    //this is important to undersand that if already present in the set. neglect that recursive tree simmce that 
    //will give the same permutations as earlier
    //this duplicates strings are eliminated
    unordered_set<char> st;
    for (int i = ind; i < n; i++)
    {
        if (!st.count(ip[i]))
        {
            st.insert(ip[i]);

            swap(ip[i], ip[ind]);
            permutations(ans, ip, ind + 1, n);
            swap(ip[i], ip[ind]);
        }
    }
}

